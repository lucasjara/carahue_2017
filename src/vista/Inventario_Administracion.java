    private void cboCategoriaBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCategoriaBusquedaActionPerformed
        String categoria = cboCategoriaBusqueda.getSelectedItem().toString();
        if (txtNombreBusqueda.getText().equals("") && txtCodigoBusqueda.getText().equals("")) {
            if (categoria.equals("TODAS LAS CATEGORIAS")) {
                CargarTablas(1, "", "");
            } else {
                CargarTablas(4, categoria, "");
            }
        } else if (txtCodigoBusqueda.getText().equals("")) {
            //Si el codigo esta vacio significa que el nombre contiene un valor
            String nombre = txtNombreBusqueda.getText();
            if (categoria.equals("TODAS LAS CATEGORIAS")) {
                CargarTablas(2, "", nombre);
            } else {
                CargarTablas(3, categoria, nombre);
            }
        } else if (txtNombreBusqueda.getText().equals("")) {
            String codigo = txtCodigoBusqueda.getText();
            if (categoria.equals("TODAS LAS CATEGORIAS")) {
                CargarTablas(6, "", codigo);
            } else {
                CargarTablas(5, categoria, codigo);
            }
        }
        Limpiar();
    }//GEN-LAST:event_cboCategoriaBusquedaActionPerformed

